package com.lec.ex;
		// 변수 선언 방법
public class VarEx03 {
	public static void main(String[] args) {
		
		int num1 = 10;
		int num2;
		num2 = 20;
		
		System.out.printf("num1 = %d \t num2 = %d\n", num1, num2);
		
		//동일자료형 변수를 다수 선언과 초기화

		int num3 = 30, num4 = 40;
		System.out.printf("num3 = %d \t num4 = %d\n", num3, num4);

		int num5, num6;
		num5 = 50; num6 = 60;
	}
}
